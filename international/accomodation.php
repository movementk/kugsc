                                <h6 class="title">Check list before signing a lease with Gositel or Studios</h6>
                                <ol class="co-list">
                                    <li>What options does the studio/gositel come with?</li>
                                    <li>Does it have a washer and dryer?</li>
                                    <li>Is the laundry facility communal?</li>
                                    <li>What is inclusive in the monthly rent?</li>
                                    <li>Are utilities paid separately by each tenant?</li>
                                    <li>How much is the monthly maintenance fee?</li>
                                    <li>Is internet access provided?</li>
                                    <li>How is garbage disposed?</li>
                                </ol>
